package lab3.ex1;

// Java program to illustrate If statement

class IfDemo {
    public static void main(String args[]) {
        String str = "Hello World!";
        int i = 4;

        // if block
        if (i == 4) {
            i++;
            System.out.println(str);
        }

        // Executed by default
        System.out.println("i = " + i);
    }
}
