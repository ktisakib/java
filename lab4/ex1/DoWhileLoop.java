package lab4.ex1;

// Java program to illustrate do-while loop
public class DoWhileLoop {
    public static void main(String[] args) {
        int i = 2; // Initialization expression

        do {
            // loop body
            System.out.println("Hello World");

            // update expression
            i++;
        } while (i < 1); // test expression
    }
}

 